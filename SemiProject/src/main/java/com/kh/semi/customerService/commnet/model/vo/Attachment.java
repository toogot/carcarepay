package com.kh.semi.customerService.commnet.model.vo;

public class Attachment {
  private Integer attNo;
  private String attRoute;
  private String attTitle;

  public Integer getAttNo() {
    return attNo;
  }

  public void setAttNo(Integer attNo) {
    this.attNo = attNo;
  }

  public String getAttRoute() {
    return attRoute;
  }

  public void setAttRoute(String attRoute) {
    this.attRoute = attRoute;
  }

  public String getAttTitle() {
    return attTitle;
  }

  public void setAttTitle(String attTitle) {
    this.attTitle = attTitle;
  }
}
